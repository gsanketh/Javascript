var a = [1,2,3];

console.log(a);

a[3] = 4 // to push the value into array

console.log(a.length); //to know the lenght of array

a.push(5); // it pushes the value in the last element of array without knowing the lenght

a.pop(); // it removes the last value in the array

a.unshift(2); // it pushes the value in the first element on array

a.shift; // it removes the first element in the array